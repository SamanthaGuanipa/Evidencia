def square(x, y):
    """
   Por medio de esta funcion se crean cuadrados
    """
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)
    path.end_fill()


def offset(point):
    """
    Se regresa un valor de desplazamiento
    """
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """
   Regresa un valor booleano teniendo en cuenta la posicion respecto a tiles
    """
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """
    Crea las paredes, sendero y pildoras en un lienzo llamado path
    """
    bgcolor("white")
    path.color('green')

    for index in range(len(tiles)):
        tile = tiles[index]
        # Dibuja Muros
        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)
            # Dibuja pildoras
            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """
    Se define la funcion que realiza el movimiento de Pacman
    y de los fantasmas
    """
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)
    
    """
    Se sumaran los puntos y se quitaran la pildoras del tablero
    """
    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)
    
    """
    Crea a pacman
    """
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')
    
    """
    Ciclo para dibujar a los ghosts
    """
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y
        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')
    update()

    """
    Termina el juego
    """
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 5)


def change(x, y):
    """
    Valida la posicion de PacMan y luego cambiarÃ¡ su ubicacion
    """
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y


